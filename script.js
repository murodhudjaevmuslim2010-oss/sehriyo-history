// Отрисовка списка вопросов при создании
game.renderPendingQuestions = function () {
    const list = document.getElementById('questionsList');
    const count = document.getElementById('questionCount');
    const createBtn = document.getElementById('createRoomBtn');

    count.textContent = this._pendingRoomQuestions.length;

    if (this._pendingRoomQuestions.length === 0) {
        list.innerHTML = '<p class="empty-state">Пока нет вопросов. Добавьте минимум 5.</p>';
        createBtn.disabled = true;
        return;
    }

    createBtn.disabled = this._pendingRoomQuestions.length < 5;

    list.innerHTML = this._pendingRoomQuestions.map((q, i) => `
        <div class="question-item">
            <span class="question-item-num">${i + 1}</span>
            <span class="question-item-text">${q.text}</span>
            <button class="question-item-delete" onclick="game.removeQuestionFromForm(${i})">
                <i class="fas fa-trash"></i>
            </button>
        </div>
    `).join('');
};

// ——— Вспомогательные UI для асинхронных операций ———
