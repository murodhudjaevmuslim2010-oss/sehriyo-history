    // Показать экран
    showScreen(screenId) {
        // Скрыть все экраны
        document.querySelectorAll('.screen').forEach(screen => {
            screen.classList.remove('active');
        });

        // Скрыть оверлеи (пауза и т.д.)
        const pauseScreen = document.getElementById('pauseScreen');
        if (pauseScreen) pauseScreen.style.display = 'none';

        // Показать нужный экран
        document.getElementById(screenId)?.classList.add('active');

        // Особые действия для экранов
        if (screenId === 'mainMenu') {
            this.updateStats();
        } else if (screenId === 'settingsScreen') {
            this.updateSettingsUI();
        } else if (screenId === 'countrySelectionScreen') {
            this.updateCountrySelectionUI();
        } else if (screenId === 'gameScreen' && this.state.map) {
            // Invalidate map size to fix rendering issues
            setTimeout(() => {
                this.state.map.invalidateSize();
            }, 200);
        } else if (screenId === 'myRoomsScreen') {
            this.renderMyRooms();
        }
    },

