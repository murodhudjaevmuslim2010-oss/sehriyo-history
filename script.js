    // Генерация карты (Leaflet)
    generateMap() {
        const mapContainer = document.getElementById('territoryMap');
        if (!mapContainer) return;

        // Если карта каким-то образом выжила после resetGameState
        if (this.state.map) {
            try {
                this.state.map.remove();
            } catch (e) { }
        }

        // Очищаем контейнер физически
        mapContainer.innerHTML = '';
        const newMapDiv = document.createElement('div');
        newMapDiv.id = 'map-helper-' + Date.now();
        newMapDiv.style.width = '100%';
        newMapDiv.style.height = '100%';
        // В данном случае лучше просто использовать id 'territoryMap', но убедиться что он чист
        mapContainer.innerHTML = '';

        // Инициализируем карту
        this.state.map = L.map('territoryMap', {
            zoomControl: false,
            attributionControl: false,
            preferCanvas: true // Optimization
        }).setView([20, 0], 2);

        // Zoom control
        L.control.zoom({ position: 'topright' }).addTo(this.state.map);

        // Base layers
        const cartoDark = L.tileLayer('https://{s}.basemaps.cartocdn.com/dark_all/{z}/{x}/{y}{r}.png', {
            attribution: '&copy; OpenStreetMap &copy; CARTO',
            subdomains: 'abcd',
            maxZoom: 19
        });

        const cartoVoyager = L.tileLayer('https://{s}.basemaps.cartocdn.com/rastertiles/voyager/{z}/{x}/{y}{r}.png', {
            attribution: '&copy; OpenStreetMap &copy; CARTO',
            subdomains: 'abcd',
            maxZoom: 19
        });

        // Store layers to switch later
        this.state.baseLayers = {
            dark: cartoDark,
            light: cartoVoyager
        };

        // Select base layer based on theme
        if (this.settings.theme === 'dark') {
            cartoDark.addTo(this.state.map);
        } else {
            cartoVoyager.addTo(this.state.map);
        }

        // Load GeoJSON
        this.loadTerritories();
    },

    async loadTerritories() {
        try {
            document.getElementById('mapTitle').textContent = "Загрузка всего мира...";

            let data;
            if (this.geoJsonCache) {
                data = this.geoJsonCache;
            } else {
                // Пытаемся загрузить локальный файл, если не выйдет - берем из сети
                try {
                    const localResponse = await fetch('assets/data/countries.geojson');
                    if (localResponse.ok) {
                        data = await localResponse.json();
                    }
                } catch (e) {
                    console.warn("Local GeoJSON not found, fetching from GitHub...");
                }

                if (!data) {
                    const response = await fetch('https://raw.githubusercontent.com/johan/world.geo.json/master/countries.geo.json');
                    if (!response.ok) throw new Error("Network response was not ok");
                    data = await response.json();
                }
                this.geoJsonCache = data;
            }

            this.generateTerritoryPolygons(data);

            if (this.state.geoJsonLayer) {
                const bounds = this.state.geoJsonLayer.getBounds();
                if (bounds.isValid()) {
                    this.state.map.fitBounds(bounds, { padding: [50, 50] });
                }
            }

            document.getElementById('mapTitle').textContent = "Карта Мира";
        } catch (e) {
            console.error("Failed to load map data", e);
            document.getElementById('mapTitle').textContent = "Ошибка загрузки карты";
        }
    },



