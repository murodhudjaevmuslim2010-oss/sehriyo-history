game.isAdjacentToOwned = function (territoryId, owner) {
    const neighbors = this.state.politicalMode.territoryNeighbors[territoryId] || [];
    return neighbors.some(nId => {
        const neighbor = this.state.territories.find(t => t.id === nId);
        return neighbor && neighbor.captured && neighbor.owner === owner;
    });
};

